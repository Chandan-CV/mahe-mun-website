Nations (MUN) conferences play a vital role in nurturing diplomacy, critical thinking,
and global awareness among students. MAHE MUN 2023, hosted by MAHE Bengaluru
Campus on October 7th and 8th, 2023, carries forward the tradition of fostering
international understanding and youth leadership.</p>
<br>
<p class="text-left">
The past two editions, hosted by MIT Bengaluru, a constituent unit of MAHE Bengaluru,
were highly successful, drawing participation from diverse colleges. These events
provided students with the opportunity to engage with global issues, learn negotiation
strategies, and experience the complexities of international relations firsthand. This
edition, organized by our entire university, promises to be even more significant and
thought-provoking.
</p>
<br>
<p class="text-left">
This year's conference will feature five committees, each representing distinct aspects
of global diplomacy:
<br>
<br>
<ul class="text-left" style="list-style-type:disc;">
        <li> United Nations Security Council (UNSC) - mirroring its real-world counterpart instructure and function.</li>
        <li>United Nations General Assembly (UNGA) - addressing matters of universal concern and promoting international cooperation.</li>
        <li>World Health Organisation(WHO) - a specialized agency of the United Nations responsible for international public health.</li>
        <li>Continuous Crisis Committee - known for high-pressure discussions, quick decision-making, and innovative problem-solving.</li>
</ul>
<br>
<p class="text-left">
In addition to the committees, the International Press Corps will provide a space for
independent journalistic thought and analysis. Delegates will have access to
refreshments and amenities to ensure an ideal environment for dialogue and
collaboration.
</p>
